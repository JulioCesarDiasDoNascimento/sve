package com.unp.sve.domain;

public enum Partidos {

    /**
     * Partido dos Trabalhadores - Maior partido de esquerda do Brasil, fundado em 1980
     */
    PT,
    
    /**
     * Partido Liberal - Um dos maiores partidos de direita, forte representação no Congresso
     */
    PL,
    
    /**
     * União Brasil - Resultado da fusão do DEM com PSL, grande bancada parlamentar
     */
    UNIAO,
    
    /**
     * Progressistas - Partido tradicional de centro-direita com forte base regional
     */
    PP,
    
    /**
     * Movimento Democrático Brasileiro - Maior partido de centro, antiga ARENA/PMDB
     */
    MDB,
    
    /**
     * Partido Social Democrático - Partido de centro com grande representatividade
     */
    PSD,
    
    /**
     * Republicanos - Partido com forte vínculo religioso e conservador
     */
    REPUBLICANOS,
    
    /**
     * Partido da Social Democracia Brasileira - Tradicional partido de centro-esquerda
     */
    PSDB,
    
    /**
     * Partido Socialista Brasileiro - Partido histórico de centro-esquerda
     */
    PSB,
    
    /**
     * Partido Democrático Trabalhista - Partido trabalhista histórico de centro-esquerda
     */
    PDT,

    /**
     * Movimento Brasil Livre - Um movimento político com foco em princípios liberais,
     * defesa da liberdade econômica, diminuição do estado e combate à corrupção.
     */
    MBL;
}
